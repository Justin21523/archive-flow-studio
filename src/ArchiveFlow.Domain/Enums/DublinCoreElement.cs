namespace ArchiveFlow.Domain.Enums;

/// <summary>
/// Represents the 15 core elements of the Dublin Core Metadata Initiative (DCMI).
/// These are the international standard for describing digital resources.
/// </summary>
public enum DublinCoreElement
{
    Title,
    Creator,
    Subject,
    Description,
    Publisher,
    Contributor,
    Date,
    Type,
    Format,
    Identifier,
    Source,
    Language,
    Relation,
    Coverage,
    Rights
}